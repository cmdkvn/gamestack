import type { BenchmarkReport, Diff, NumericStat } from "./benchmark.ts";

export function renderMarkdown(report: BenchmarkReport): string {
  const lines: string[] = [];
  lines.push(`# Game perf benchmark — ${report.scenario}`);
  lines.push("");
  lines.push(`- **Generated:** ${report.generatedAt}`);
  lines.push(`- **Platform:** ${report.platform}`);
  lines.push(`- **Endpoint:** ${report.endpoint}`);
  lines.push(`- **Scene:** ${report.scene ?? "(not reported)"}`);
  lines.push(`- **Duration:** ${report.durationSeconds}s`);
  lines.push(`- **Samples collected:** ${report.aggregates.sampleCount}`);
  lines.push("");
  lines.push("## Current snapshot");
  lines.push("");
  lines.push("| Metric | Mean | Min | Max | P99 | P99.9 |");
  lines.push("|---|---:|---:|---:|---:|---:|");
  pushStat(lines, "FPS", report.aggregates.fps, 1);
  pushStat(lines, "Frame time (ms)", report.aggregates.frameTimeMs, 2);
  pushStat(lines, "Draw calls / frame", report.aggregates.drawCalls, 0);
  pushStat(lines, "Batches / frame", report.aggregates.batches, 0);
  pushStat(lines, "GC alloc / frame (B)", report.aggregates.gcAllocBytesPerFrame, 0);
  lines.push(`| Peak memory (MB) | ${report.aggregates.memoryMBPeak.toFixed(1)} | — | — | — | — |`);
  lines.push("");

  if (report.baselineCompare && report.baselineCompare.length > 0) {
    lines.push("## Diff vs baseline");
    lines.push("");
    lines.push("| Metric | Current | Baseline | Δ | % | Verdict |");
    lines.push("|---|---:|---:|---:|---:|---|");
    for (const d of report.baselineCompare) lines.push(rowForDiff(d));
    lines.push("");
  } else {
    lines.push("## Diff vs baseline");
    lines.push("");
    lines.push("_No baseline provided. Save this snapshot as the baseline for future runs._");
    lines.push("");
  }

  if (report.regressions.length > 0) {
    lines.push("## Regressions");
    lines.push("");
    for (const r of report.regressions) {
      lines.push(`- **${r.metric}** regressed: ${formatNumber(r.current)} vs baseline ${formatNumber(r.baseline)} (${formatPct(r.deltaPct)})`);
    }
    lines.push("");
  }

  if (report.notes.length > 0) {
    lines.push("## Notes");
    lines.push("");
    for (const n of report.notes) lines.push(`- ${n}`);
    lines.push("");
  }
  lines.push("---");
  lines.push("");
  lines.push("Generated by `gamestack-game-benchmark`. The interactive `/perf-benchmark` skill walks the regressions and proposes investigation hypotheses.");
  lines.push("");
  return lines.join("\n");
}

function pushStat(lines: string[], label: string, stat: NumericStat, digits: number): void {
  if (stat.count === 0) {
    lines.push(`| ${label} | — | — | — | — | — |`);
    return;
  }
  lines.push(
    `| ${label} | ${stat.mean.toFixed(digits)} | ${stat.min.toFixed(digits)} | ${stat.max.toFixed(digits)} | ${stat.p99.toFixed(digits)} | ${stat.p999.toFixed(digits)} |`,
  );
}

function rowForDiff(d: Diff): string {
  return `| ${d.metric} | ${formatNumber(d.current)} | ${formatNumber(d.baseline)} | ${formatNumber(d.deltaAbs)} | ${formatPct(d.deltaPct)} | ${d.verdict} |`;
}

function formatNumber(n: number): string {
  if (Number.isInteger(n)) return n.toString();
  return Math.abs(n) >= 100 ? n.toFixed(1) : n.toFixed(2);
}

function formatPct(p: number | null): string {
  if (p === null) return "—";
  const v = p * 100;
  return (v >= 0 ? "+" : "") + v.toFixed(1) + "%";
}
