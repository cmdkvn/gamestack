import type { CertReport, PlatformReport, CategoryResult } from "./check.ts";
import type { Verdict } from "../shared/cert-categories.ts";
import { padRight } from "../shared/format.ts";

const VERDICT_TAG: Record<Verdict, string> = {
  PASS: "PASS",
  PASS_CODE_ONLY: "PASS_CODE_ONLY",
  NEEDS_LIVE_TEST: "NEEDS_LIVE_TEST",
  FAIL_P0: "FAIL_P0",
  FAIL_P1: "FAIL_P1",
  NOT_APPLICABLE: "N/A",
};

export function renderMarkdown(report: CertReport): string {
  const lines: string[] = [];
  lines.push("# Cert readiness");
  lines.push("");
  lines.push(`- **Generated:** ${report.generatedAt}`);
  lines.push(`- **Project:** ${report.projectRoot}`);
  lines.push(`- **Engine:** ${report.engine}`);
  lines.push(`- **Platforms audited:** ${report.platforms.map((p) => p.displayName).join(", ")}`);
  lines.push("");

  lines.push("> **Reminder.** " + report.reminder);
  lines.push("");

  for (const pr of report.platforms) {
    lines.push(...renderPlatform(pr));
    lines.push("");
  }

  lines.push("## Combined action list");
  lines.push("");
  if (report.actionList.p0.length === 0 && report.actionList.p1.length === 0 && report.actionList.needsLiveTest.length === 0) {
    lines.push("_No actions outstanding._");
    lines.push("");
  } else {
    lines.push("### P0 — fix before submission");
    lines.push("");
    if (report.actionList.p0.length === 0) {
      lines.push("_None._");
    } else {
      report.actionList.p0.forEach((a, i) => {
        lines.push(`${i + 1}. **${a.platform} · ${a.category}** — ${a.action}`);
      });
    }
    lines.push("");

    lines.push("### P1 — strongly recommended");
    lines.push("");
    if (report.actionList.p1.length === 0) {
      lines.push("_None._");
    } else {
      report.actionList.p1.forEach((a, i) => {
        lines.push(`${i + 1}. **${a.platform} · ${a.category}** — ${a.action}`);
      });
    }
    lines.push("");

    lines.push("### Needs live test — run these scenarios");
    lines.push("");
    if (report.actionList.needsLiveTest.length === 0) {
      lines.push("_None._");
    } else {
      report.actionList.needsLiveTest.forEach((a, i) => {
        lines.push(`${i + 1}. **${a.platform} · ${a.category}** — ${a.action}`);
      });
    }
    lines.push("");
  }

  lines.push("### Submission readiness per platform");
  lines.push("");
  for (const pr of report.platforms) {
    lines.push(`- **${pr.displayName}:** ${pr.submissionReadiness} — ${pr.readinessReason}`);
  }
  lines.push("");
  lines.push("---");
  lines.push("");
  lines.push("Generated by `gamestack-cert-checklist`. The interactive `/cert-readiness` skill cross-references the latest TRC/TCR/lotcheck PDF with the same categories.");
  lines.push("");
  return lines.join("\n");
}

function renderPlatform(pr: PlatformReport): string[] {
  const lines: string[] = [];
  lines.push(`## ${pr.displayName}`);
  lines.push("");
  lines.push(`- **Checklist version on file:** ${pr.checklistVersionOnFile ?? "**NOT FOUND — P0**"}`);
  lines.push(`- **Build:** ${pr.buildRev ?? "(no git)"}`);
  lines.push("");

  const c = pr.counts;
  lines.push("| PASS | PASS-CODE-ONLY | NEEDS-LIVE-TEST | FAIL-P0 | FAIL-P1 | N/A |");
  lines.push("|---:|---:|---:|---:|---:|---:|");
  lines.push(`| ${c.PASS} | ${c.PASS_CODE_ONLY} | ${c.NEEDS_LIVE_TEST} | ${c.FAIL_P0} | ${c.FAIL_P1} | ${c.NOT_APPLICABLE} |`);
  lines.push("");

  lines.push("| Category | Verdict | Detail |");
  lines.push("|---|---|---|");
  for (const r of pr.results) {
    lines.push(`| ${r.category.name} | ${VERDICT_TAG[r.verdict]} | ${truncate(r.detail, 120)} |`);
  }
  lines.push("");
  return lines;
}

function truncate(s: string, n: number): string {
  return s.length <= n ? s : s.slice(0, n - 1) + "…";
}
