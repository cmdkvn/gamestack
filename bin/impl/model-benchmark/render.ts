import type { BenchmarkReport, CaseResult } from "./types.ts";

export function renderMarkdown(report: BenchmarkReport): string {
  const lines: string[] = [];
  lines.push(`# Model benchmark — ${report.suite.name}`);
  lines.push("");
  lines.push(`- **Generated:** ${report.generatedAt}`);
  if (report.suite.description) lines.push(`- **Description:** ${report.suite.description}`);
  lines.push(`- **Models:** ${report.models.join(", ")}`);
  lines.push(`- **Cases per model:** ${report.cases}`);
  if (report.winner) lines.push(`- **Winner:** ${report.winner}`);
  lines.push("");

  lines.push("## Summary");
  lines.push("");
  lines.push("| Model | Pass | Pass rate | Mean latency (ms) | P99 latency (ms) | Mean output tokens | Total tokens (in / out) |");
  lines.push("|---|---:|---:|---:|---:|---:|---:|");
  for (const s of report.summaries) {
    lines.push(
      `| ${s.model} | ${s.passed}/${s.totalCases} | ${(s.passRate * 100).toFixed(0)}% | ${s.meanLatencyMs} | ${s.p99LatencyMs} | ${s.meanOutputTokens} | ${s.totalInputTokens} / ${s.totalOutputTokens} |`,
    );
  }
  lines.push("");

  // Per-case detail grouped by case id.
  const byCase = new Map<string, CaseResult[]>();
  for (const r of report.results) {
    if (!byCase.has(r.caseId)) byCase.set(r.caseId, []);
    byCase.get(r.caseId)!.push(r);
  }

  lines.push("## Per-case results");
  lines.push("");
  for (const [caseId, runs] of byCase) {
    lines.push(`### Case: \`${caseId}\``);
    lines.push("");
    lines.push("| Model | Verdict | Failed checks | Latency (ms) | Output tokens |");
    lines.push("|---|---|---|---:|---:|");
    for (const r of runs) {
      const failed = r.checks.filter((c) => !c.passed).map((c) => c.name).join("; ") || "—";
      lines.push(`| ${r.model} | ${r.passed ? "PASS" : "FAIL"} | ${failed} | ${r.latencyMs} | ${r.outputTokens} |`);
    }
    lines.push("");
  }

  if (report.notes.length > 0) {
    lines.push("## Notes");
    lines.push("");
    for (const n of report.notes) lines.push(`- ${n}`);
    lines.push("");
  }
  lines.push("---");
  lines.push("");
  lines.push("Generated by `gamestack-model-benchmark`. Local results only — the CLI does not upload runs to a remote.");
  lines.push("");
  return lines.join("\n");
}
