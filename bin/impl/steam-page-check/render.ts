import type { SteamPageReport } from "./check.ts";

export function renderMarkdown(report: SteamPageReport): string {
  const lines: string[] = [];
  lines.push("# Steam page check");
  lines.push("");
  lines.push(`- **Generated:** ${report.generatedAt}`);
  lines.push(`- **Project:** ${report.projectRoot}`);
  lines.push(`- **Steam directory:** ${report.steamDir}`);
  lines.push(`- **App ID:** ${report.appId ?? "(unknown)"}`);
  lines.push(`- **Page state:** ${report.pageStateRaw ?? "(unknown)"}`);
  lines.push("");

  lines.push("## Capsules");
  lines.push("");
  lines.push("| Slot | Expected | File | Verdict | Reason |");
  lines.push("|---|---|---|---|---|");
  for (const c of report.capsules) {
    const file = c.file ?? "—";
    const verdictBadge = c.verdict === "PASS" ? "PASS" : c.verdict === "FAIL" ? "FAIL" : "MISSING";
    lines.push(`| ${c.slot} | ${c.expectedWidth}×${c.expectedHeight} | \`${file}\` | ${verdictBadge} | ${c.reason} |`);
  }
  lines.push("");

  lines.push("## Trailer");
  lines.push("");
  lines.push(`- **Length:** ${report.trailer.lengthSec ?? "(unknown)"}${report.trailer.lengthSec !== null ? "s" : ""}`);
  lines.push(`- **First-6s verb landed:** ${formatBool(report.trailer.firstSixSecondVerb)}`);
  lines.push(`- **Studio card at start:** ${formatBool(report.trailer.studioCardAtStart, true)}`);
  lines.push(`- **Subtitles default on:** ${formatBool(report.trailer.hasSubtitles)}`);
  if (report.trailer.reasons.length > 0) {
    lines.push("");
    lines.push("**Concerns:**");
    for (const r of report.trailer.reasons) lines.push(`- ${r}`);
  }
  lines.push("");

  if (report.shortDescription) {
    const sd = report.shortDescription;
    lines.push("## Short description");
    lines.push("");
    lines.push(`- **Character count:** ${sd.characterCount} / 300`);
    lines.push(`- **Within limit:** ${formatBool(sd.withinLimit)}`);
    lines.push(`- **Genre word present:** ${formatBool(sd.genreWordPresent)}`);
    lines.push(`- **Cliché flags:** ${sd.clicheFlags.length === 0 ? "none" : sd.clicheFlags.join(", ")}`);
    lines.push(`- **Feature-list anti-pattern:** ${formatBool(sd.featureListFlag, true)}`);
    lines.push("");
  } else {
    lines.push("## Short description");
    lines.push("");
    lines.push("_No short description found in page.json._");
    lines.push("");
  }

  if (report.tags) {
    const t = report.tags;
    lines.push("## Tags");
    lines.push("");
    lines.push(`- **Count:** ${t.count} (recommended 5–15)`);
    lines.push(`- **Genre-leading tags:** ${t.genreTagCount} (target 3–5)`);
    lines.push(`- **Vibe tags:** ${t.vibeTagCount} (target 2–3)`);
    if (t.unknownTags.length > 0) {
      lines.push(`- **Unknown / custom tags:** ${t.unknownTags.join(", ")}`);
    }
    if (t.recommendedAdditions.length > 0) {
      lines.push("");
      lines.push("**Recommendations:**");
      for (const r of t.recommendedAdditions) lines.push(`- ${r}`);
    }
    lines.push("");
  } else {
    lines.push("## Tags");
    lines.push("");
    lines.push("_No tags found in page.json._");
    lines.push("");
  }

  lines.push("## Screenshots");
  lines.push("");
  lines.push(`- **Count:** ${report.screenshots.count}`);
  lines.push(`- **Meets ≥5 minimum:** ${formatBool(report.screenshots.withinMinimum)}`);
  lines.push("");

  if (report.nextFest) {
    lines.push("## Next Fest fit");
    lines.push("");
    lines.push(`- **Planned:** ${formatBool(report.nextFest.planned)}`);
    if (report.nextFest.planned) {
      lines.push(`- **Demo length OK:** ${formatBool(report.nextFest.demoLengthOk)}`);
      lines.push(`- **Demo ends on hook:** ${formatBool(report.nextFest.demoEndsOnHook)}`);
      if (report.nextFest.reasons.length > 0) {
        lines.push("");
        for (const r of report.nextFest.reasons) lines.push(`- ${r}`);
      }
    }
    lines.push("");
  }

  if (report.topEdits.length > 0) {
    lines.push("## Top edits (by wishlist-conversion impact)");
    lines.push("");
    for (const e of report.topEdits) {
      lines.push(`${e.rank}. **${e.edit}**`);
      lines.push(`    - ${e.expectedEffect}`);
    }
    lines.push("");
  }

  lines.push(`## Wishlist-conversion risk: ${report.wishlistRisk}`);
  lines.push("");
  lines.push(report.wishlistRiskReason);
  lines.push("");
  lines.push("---");
  lines.push("");
  lines.push("Generated by `gamestack-steam-page-check`. The interactive `/steam-page-review` skill compares the page against current top-sellers in your genre and proposes the human-judgment edits the CLI can't make.");
  lines.push("");
  return lines.join("\n");
}

function formatBool(b: boolean | null, invert = false): string {
  if (b === null) return "unknown";
  const effective = invert ? !b : b;
  return effective ? "✓" : "✗";
}
