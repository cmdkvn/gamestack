import type { TasteProfile } from "./profile.ts";

export function renderMarkdown(profile: TasteProfile, opts: { title?: string } = {}): string {
  const lines: string[] = [];
  lines.push(`# ${opts.title ?? "Taste profile"}`);
  lines.push("");
  lines.push(`- **Updated:** ${profile.updatedAt}`);
  lines.push(`- **Rounds logged:** ${profile.rounds.length}`);
  lines.push(`- **Axes tracked:** ${Object.keys(profile.preferences.byAxis).length}`);
  lines.push("");

  if (profile.emergingSignals.length > 0) {
    lines.push("## Emerging signals");
    lines.push("");
    for (const s of profile.emergingSignals) lines.push(`- ${s}`);
    lines.push("");
  }

  const axisKeys = Object.keys(profile.preferences.byAxis).sort();
  if (axisKeys.length > 0) {
    lines.push("## Preferences by axis");
    lines.push("");
    for (const axis of axisKeys) {
      lines.push(`### ${axis}`);
      lines.push("");
      lines.push("| Value | Wins | Losses | Win rate | Last updated |");
      lines.push("|---|---:|---:|---:|---|");
      const bucket = profile.preferences.byAxis[axis]!;
      const rows = Object.entries(bucket)
        .map(([value, stat]) => {
          const total = stat.wins + stat.losses;
          const wr = total > 0 ? (stat.wins / total) * 100 : 0;
          return { value, stat, total, wr };
        })
        .sort((a, b) => b.wr - a.wr || b.total - a.total);
      for (const r of rows) {
        lines.push(
          `| ${r.value} | ${r.stat.wins} | ${r.stat.losses} | ${r.total > 0 ? r.wr.toFixed(0) + "%" : "—"} | ${r.stat.lastUpdated.slice(0, 10)} |`,
        );
      }
      lines.push("");
    }
  }

  if (profile.preferences.negativePatterns.length > 0) {
    lines.push("## Negative patterns");
    lines.push("");
    for (const np of profile.preferences.negativePatterns) lines.push(`- ${np}`);
    lines.push("");
  }

  if (profile.rounds.length > 0) {
    lines.push("## Recent rounds");
    lines.push("");
    const recent = profile.rounds.slice(-5);
    for (const r of recent) {
      lines.push(`### ${r.subject}${r.round !== undefined ? ` (round ${r.round})` : ""} — ${r.date}`);
      lines.push("");
      for (const v of r.variants) {
        const conf = v.confidence ? ` [${v.confidence}]` : "";
        lines.push(`- **${v.variant}** · ${v.axisVaried}=${v.axisValue} · ${v.outcome}${conf}${v.notes ? ` — ${v.notes}` : ""}`);
      }
      lines.push("");
    }
  }
  lines.push("---");
  lines.push("");
  lines.push("Generated by `gamestack-taste-update`. Pair with `/art-shotgun` for the interactive round-by-round flow.");
  lines.push("");
  return lines.join("\n");
}
