import type { RunReport } from "./runner.ts";
import type { StepResult } from "../shared/scenario.ts";

export function renderMarkdown(report: RunReport): string {
  const lines: string[] = [];
  lines.push(`# Playtest run — ${report.scenario.name}`);
  lines.push("");
  lines.push(`- **Generated:** ${report.generatedAt}`);
  lines.push(`- **Endpoint:** ${report.endpoint}`);
  lines.push(`- **Phase:** ${report.scenario.phase}`);
  lines.push(`- **Run directory:** ${report.runDir}`);
  lines.push(`- **Started:** ${report.startedAt}`);
  lines.push(`- **Finished:** ${report.finishedAt}`);
  lines.push(`- **Total elapsed:** ${report.totalElapsedMs}ms`);
  if (report.durationBudgetExceeded) {
    lines.push(`- **Budget exceeded:** YES (max_duration_seconds=${report.scenario.max_duration_seconds})`);
  }
  lines.push(`- **Outcome:** ${report.passed ? "PASS" : "FAIL"}`);
  lines.push("");
  if (report.scenario.description) {
    lines.push(`> ${report.scenario.description}`);
    lines.push("");
  }

  renderPhase(lines, "Setup", report.phases.setup);
  renderPhase(lines, "Steps", report.phases.steps);
  renderPhase(lines, "Teardown", report.phases.teardown);

  if (report.scenario.assertions_summary && report.scenario.assertions_summary.length > 0) {
    lines.push("## Asserted (human-readable summary)");
    lines.push("");
    for (const a of report.scenario.assertions_summary) lines.push(`- ${a}`);
    lines.push("");
  }

  lines.push("---");
  lines.push("");
  lines.push("Generated by `gamestack-playtest-daemon`. Drives a running Unity / Godot build via the gamestack engine SDK.");
  lines.push("");
  return lines.join("\n");
}

function renderPhase(lines: string[], title: string, results: StepResult[]): void {
  if (results.length === 0) return;
  lines.push(`## ${title}`);
  lines.push("");
  lines.push("| # | Step | Type | Status | Elapsed (ms) | Detail |");
  lines.push("|---:|---|---|---|---:|---|");
  results.forEach((r, i) => {
    const detail = formatDetail(r);
    lines.push(`| ${i + 1} | ${r.step} | ${r.type} | ${r.status} | ${r.elapsed_ms} | ${detail} |`);
  });
  lines.push("");
}

function formatDetail(r: StepResult): string {
  if (!r.details) return "—";
  if (r.status === "fail" || r.status === "timeout") return String(r.details.reason ?? "—");
  return Object.entries(r.details)
    .map(([k, v]) => `${k}=${JSON.stringify(v)}`)
    .join("; ");
}
