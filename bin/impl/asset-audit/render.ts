import type { AssetAuditReport, AssetFinding, Severity } from "./audit.ts";
import { formatBytes } from "../shared/format.ts";

const SEVERITY_RANK: Record<Severity, number> = { P0: 0, P1: 1, P2: 2, INFO: 3 };

export function renderMarkdown(report: AssetAuditReport): string {
  const lines: string[] = [];
  lines.push(`# Asset audit — ${report.platform}`);
  lines.push("");
  lines.push(`- **Generated:** ${report.generatedAt}`);
  lines.push(`- **Project:** ${report.projectRoot}`);
  lines.push(`- **Engine:** ${report.engine}`);
  lines.push(`- **Scan roots:** ${report.scanRoots.join(", ") || "(none found)"}`);
  lines.push(`- **Asset count:** ${report.counts.files}`);
  lines.push(`- **Total bytes (incl. source artwork):** ${formatBytes(report.totals.bytes)}`);
  lines.push("");
  lines.push("## Budget");
  lines.push("");
  lines.push("| Limit | Value |");
  lines.push("|---|---|");
  lines.push(`| Texture max dim | ${report.budget.textureMax}px |`);
  lines.push(`| Total texture-memory budget | ${report.budget.totalAtlasMB} MB |`);
  lines.push(`| Audio bitrate window | ${report.budget.audioBitrateMinKbps}–${report.budget.audioBitrateMaxKbps} kbps |`);
  lines.push(`| Mesh tris (env) | ${report.budget.meshTrisEnv.toLocaleString()} |`);
  lines.push(`| Mesh tris (character) | ${report.budget.meshTrisChar.toLocaleString()} |`);
  if (report.budget.totalBuildMB) lines.push(`| Total runtime build cap | ${report.budget.totalBuildMB} MB |`);
  lines.push("");
  lines.push("## Asset totals");
  lines.push("");
  lines.push("| Category | Count | Bytes |");
  lines.push("|---|---|---|");
  lines.push(`| Textures | ${report.counts.textures} | ${formatBytes(report.totals.textureBytes)} |`);
  lines.push(`| Audio | ${report.counts.audio} | ${formatBytes(report.totals.audioBytes)} |`);
  lines.push(`| Meshes | ${report.counts.meshes} | ${formatBytes(report.totals.meshBytes)} |`);
  lines.push(`| Source artwork (LFS) | ${report.counts.sourceArtwork} | ${formatBytes(report.totals.sourceArtworkBytes)} |`);
  lines.push("");

  const sorted = [...report.findings].sort(
    (a, b) => SEVERITY_RANK[a.severity] - SEVERITY_RANK[b.severity] || a.relPath.localeCompare(b.relPath),
  );

  const sectionFor = (category: AssetFinding["category"]): string => {
    switch (category) {
      case "critical": return "Critical (P0)";
      case "texture": return "Texture violations";
      case "atlas": return "Atlas observations";
      case "audio": return "Audio violations";
      case "mesh": return "Mesh observations";
      case "naming": return "Naming convention";
      case "summary": return "Aggregate";
    }
  };

  const groups = new Map<string, AssetFinding[]>();
  for (const f of sorted) {
    const k = sectionFor(f.category);
    if (!groups.has(k)) groups.set(k, []);
    groups.get(k)!.push(f);
  }

  const order = ["Critical (P0)", "Aggregate", "Texture violations", "Atlas observations", "Audio violations", "Mesh observations", "Naming convention"];
  for (const section of order) {
    const items = groups.get(section);
    if (!items || items.length === 0) continue;
    lines.push(`## ${section}`);
    lines.push("");
    for (const f of items) {
      lines.push(`- **[${f.severity}]** \`${f.relPath}\` — ${f.detail}`);
      lines.push(`    - Proposal: ${f.proposal}`);
    }
    lines.push("");
  }

  lines.push("## Summary");
  lines.push("");
  lines.push(`- **Total runtime bytes:** ${formatBytes(report.totals.bytes - report.totals.sourceArtworkBytes)}`);
  lines.push(`- **Under build cap:** ${report.underBudget ? "yes" : "NO"}`);
  lines.push(`- **Findings:** ${countBy(sorted)}`);
  lines.push("");

  if (report.proposedActions.length > 0) {
    lines.push("## Proposed actions (ordered by impact)");
    lines.push("");
    for (const a of report.proposedActions) {
      const savings = a.estSavingsBytes ? ` — est. savings ${formatBytes(a.estSavingsBytes)}` : "";
      lines.push(`${a.rank}. ${a.action}${savings}`);
    }
    lines.push("");
  }

  lines.push("---");
  lines.push("");
  lines.push("Generated by `gamestack-asset-audit`. The interactive `/asset-audit` skill provides deeper engine-specific analysis (atlas packing ratios, importer settings).");
  lines.push("");
  return lines.join("\n");
}

function countBy(findings: AssetFinding[]): string {
  const counts: Record<Severity, number> = { P0: 0, P1: 0, P2: 0, INFO: 0 };
  for (const f of findings) counts[f.severity] += 1;
  return `P0=${counts.P0} · P1=${counts.P1} · P2=${counts.P2} · INFO=${counts.INFO}`;
}
