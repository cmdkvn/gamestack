import type { LintFinding, LintReport, Severity } from "./lint.ts";

const SEV_RANK: Record<Severity, number> = { ERROR: 0, WARN: 1, INFO: 2 };

export function renderMarkdown(report: LintReport): string {
  const lines: string[] = [];
  lines.push("# gamestack — skill regression lint");
  lines.push("");
  lines.push(`- **Generated:** ${report.generatedAt}`);
  lines.push(`- **Root:** ${report.gamestackRoot}`);
  lines.push(`- **Skills checked:** ${report.skillCount}`);
  lines.push(`- **Scenarios checked:** ${report.scenarioCount}`);
  lines.push(`- **Findings:** ERROR=${report.counts.ERROR} · WARN=${report.counts.WARN} · INFO=${report.counts.INFO}`);
  lines.push(`- **Verdict:** ${report.passed ? "PASS" : "FAIL"}`);
  lines.push("");

  const sorted = [...report.findings].sort(
    (a, b) => SEV_RANK[a.severity] - SEV_RANK[b.severity] || a.path.localeCompare(b.path),
  );

  if (sorted.length === 0) {
    lines.push("_No findings. All skills, scenarios, and cross-links validate._");
    lines.push("");
  } else {
    lines.push("## Findings");
    lines.push("");
    lines.push("| Severity | Rule | Path | Detail |");
    lines.push("|---|---|---|---|");
    for (const f of sorted) {
      lines.push(`| ${f.severity} | \`${f.rule}\` | \`${f.path}\` | ${escape(f.detail)} |`);
    }
    lines.push("");
  }

  lines.push("---");
  lines.push("");
  lines.push("Generated by `gamestack-skill-lint`. Wire this into CI to catch SKILL.md drift before merging.");
  lines.push("");
  return lines.join("\n");
}

function escape(s: string): string {
  return s.replace(/\|/g, "\\|");
}

export function summarizeFindings(findings: LintFinding[]): string {
  if (findings.length === 0) return "0 findings";
  const counts: Record<Severity, number> = { ERROR: 0, WARN: 0, INFO: 0 };
  for (const f of findings) counts[f.severity] += 1;
  return `${counts.ERROR} ERROR · ${counts.WARN} WARN · ${counts.INFO} INFO`;
}
